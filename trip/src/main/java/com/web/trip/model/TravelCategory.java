package com.web.trip.model;

public class TravelCategory {
	private int travel_cate_num;
	private String travel_cate_city;
	private String travel_cate_state;

	public int getTravel_cate_num() {
		return travel_cate_num;
	}

	public void setTravel_cate_num(int travel_cate_num) {
		this.travel_cate_num = travel_cate_num;
	}

	public String getTravel_cate_city() {
		return travel_cate_city;
	}

	public void setTravel_cate_city(String travel_cate_city) {
		this.travel_cate_city = travel_cate_city;
	}

	public String getTravel_cate_state() {
		return travel_cate_state;
	}

	public void setTravel_cate_state(String travel_cate_state) {
		this.travel_cate_state = travel_cate_state;
	}

}
