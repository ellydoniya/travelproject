public class ShopCategoryDTO {
	private int shop_cate_num;
	private String shop_cate_code;
	private String shop_cate_name;

	public int getShop_cate_num() {
		return shop_cate_num;
	}

	public void setShop_cate_num(int shop_cate_num) {
		this.shop_cate_num = shop_cate_num;
	}

	public String getShop_cate_code() {
		return shop_cate_code;
	}

	public void setShop_cate_code(String shop_cate_code) {
		this.shop_cate_code = shop_cate_code;
	}

	public String getShop_cate_name() {
		return shop_cate_name;
	}

	public void setShop_cate_name(String shop_cate_name) {
		this.shop_cate_name = shop_cate_name;
	}

}
