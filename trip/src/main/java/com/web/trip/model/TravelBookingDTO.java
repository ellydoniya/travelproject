package com.web.trip.model;

public class TravelBookingDTO {

	private int book_num;
	private int prod_num;
	private int pack_num;
	private String member_id;
	private String book_email;
	private int book_person;
	private String book_date;
	private int book_price;
	private int book_check;

	public int getBook_num() {
		return book_num;
	}

	public void setBook_num(int book_num) {
		this.book_num = book_num;
	}

	public int getProd_num() {
		return prod_num;
	}

	public void setProd_num(int prod_num) {
		this.prod_num = prod_num;
	}

	public int getPack_num() {
		return pack_num;
	}

	public void setPack_num(int pack_num) {
		this.pack_num = pack_num;
	}

	public String getMember_id() {
		return member_id;
	}

	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}

	public String getBook_email() {
		return book_email;
	}

	public void setBook_email(String book_email) {
		this.book_email = book_email;
	}

	public int getBook_person() {
		return book_person;
	}

	public void setBook_person(int book_person) {
		this.book_person = book_person;
	}

	public String getBook_date() {
		return book_date;
	}

	public void setBook_date(String book_date) {
		this.book_date = book_date;
	}

	public int getBook_price() {
		return book_price;
	}

	public void setBook_price(int book_price) {
		this.book_price = book_price;
	}

	public int getBook_check() {
		return book_check;
	}

	public void setBook_check(int book_check) {
		this.book_check = book_check;
	}

}
